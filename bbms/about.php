     <h2 align="center" style="color: red">About Blood Bank</h2>
	<br><br>
	<p align="center">A blood bank collects, separates, tests and stores blood until a patient needs it. If youve donated blood before, youve been part of the blood banking process.</p>

<p align ="center">The first blood bank in the United States was established in 1937 by Dr. Bernard Fantus at Cook County Hospital in Chicago. At the time, it was not possible to store blood for more than a few days. Most transfusions were person-to-person, a dangerous process which made it hard to find donors.</p>

<p align="center">Hoping to solve this problem, Dr. Fantus began experimenting with ways to safely store blood. Initially called the Blood Preservation Laboratory, he changed the name to “blood bank” which was less likely to scare away potential donors. The ability to collect and store blood for an extended period of time revolutionized how doctors treated patients. Donating blood was no longer a high-risk process and patients in need had access to blood they needed for survival.</p>

<p align="center">Over the years, blood banking has helped save countless lives. Today, about 13.6 million units of blood are donated per year. About 36,000 units of blood are needed each day. Thanks to advances in medical technology, blood banks can safely store blood donations, and process and screen blood to ensure safety for all.</p>
